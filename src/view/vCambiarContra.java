    void volver(){
        vPerfil vP = new vPerfil(Ac);        
            vPrincipal.Escritorio.add(vP);            
            Dimension desktopSize = vPrincipal.Escritorio.getSize();
            Dimension FrameSize = vP.getSize();
            vP.setLocation((desktopSize.width - FrameSize.width)/2, (desktopSize.height- FrameSize.height)/2);                    
            vP.toFront();
            vP.setVisible(true);            
            vPrincipal.ventana="Mi Perfil";
            this.dispose();
    }
    
    public void Cambiar(){
        try {
            String Usuario = Bandera.getUsuario();
            String Actual = TxtContraActual.getText();
            String Nueva = TxtNuevaContra.getText();
            
            Usuarios su = new Usuarios();
            su.setUsuario(Usuario);
            su.setContrasena(Actual);
            su.setNuevaContra(Nueva);
            
            boolean acceso = UsuariosCAD.cambiarContrasena(su);
            
            if(!acceso){
                LblMensajeSi.setVisible(false);
                LblMensajeNo.setVisible(true);
                TxtContraActual.setText("");
                TxtNuevaContra.setText("");
                TxtConfirmeContra.setText("");
                TxtContraActual.requestFocus();
            }else{                
                LblMensajeNo.setVisible(false);
                LblMensajeSi.setVisible(true);
                TxtContraActual.setText("");
                TxtNuevaContra.setText("");
                TxtConfirmeContra.setText("");
                TxtContraActual.requestFocus();
            }
        } catch (Exception e) {
            LblMensajeNo.setText(Bandera.getRespuesta());
        }
    }

