    private void BtnCambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCambiarActionPerformed
        //<editor-fold desc="CAMBIAR CONTRASEÑA" defaultstate="collapsed">
        String Actual = TxtContraActual.getText();
        String Nueva = TxtNuevaContra.getText();
        String Confirmar = TxtConfirmeContra.getText();
        if (!"".equals(Actual)) {
            if (!Confirmar.equals(Nueva)) {
                JOptionPane.showMessageDialog(null,"Las Contraseñas No coinciden\nVerifique de Nuevo");
                TxtNuevaContra.setText("");
                TxtConfirmeContra.setText("");
                TxtNuevaContra.requestFocus();
            }else{
                if ("".equals(Confirmar) && "".equals(Nueva)) {
                    JOptionPane.showMessageDialog(null,"La nueva contraseña debe ser\n de 5 a 20 caracteres");
                }else{
                    Cambiar();
                }
            }
        }else{
            JOptionPane.showMessageDialog(null,"El campo de 'CONTRASEÑA ACTUAL' se encuentra vacio\nPor favor Llenar todos los campos");
            TxtContraActual.requestFocus();
        }
        //</editor-fold>
    }//GEN-LAST:event_BtnCambiarActionPerformed


