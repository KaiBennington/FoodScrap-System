    private void BtnVolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnVolverActionPerformed
        //<editor-fold desc="VOLVER" defaultstate="collapsed">
        if (!TxtContraActual.getText().equalsIgnoreCase("") || !TxtNuevaContra.getText().equalsIgnoreCase("") || !TxtConfirmeContra.getText().equalsIgnoreCase("")) {
            int info = JOptionPane.showConfirmDialog(null,"Desea descartar los cambios ?");
            if (info==0) {
                volver();
            }
        }else{
            volver();
        }
        //</editor-fold>
    }//GEN-LAST:event_BtnVolverActionPerformed

